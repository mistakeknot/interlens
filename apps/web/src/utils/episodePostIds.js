// Helper function to generate deep link URL
export function generateDeepLink(episode, lensName) {
  const postId = EPISODE_POST_IDS[episode];
  
  if (!postId) {
    // Fallback to episode URL if no post ID mapping exists
    return `https://read.fluxcollective.org/p/${episode}`;
  }
  
  // Convert lens name to URL-friendly slug
  const slug = lensName
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');
  
  return `https://read.fluxcollective.org/i/${postId}/${slug}`;
}

// Helper function to generate episode URL (fallback)
export function generateEpisodeUrl(episode) {
  return `https://read.fluxcollective.org/p/${episode}`;
}